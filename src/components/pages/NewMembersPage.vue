                This is not automatic upon being hired. Part of the membership application is a
                <strong>dues checkoff form</strong>. This authorizes your employer to deduct dues
                directly from your paychecks. Once submitted, membership will be approved by WGAE
                Council (strike breakers may be denied membership) and then the employer will be
                notified by WGAE staff to start deducting dues.
              </li>
            </ul>
          </div>

          <div class="space-y-2">
            <h3 class="text-base font-semibold text-(--color-brand-navy)">
              When am I covered by the contract?
            </h3>

            <ul class="list-disc space-y-2 pl-5">
              <li>
                Union contracts cover titles, not people. When someone is hired into a union
                position they are automatically entitled to the benefits of that contract.
              </li>
            </ul>
          </div>

          <div class="space-y-2">
            <h3 class="text-base font-semibold text-(--color-brand-navy)">
              Can I maintain my WGAE membership if I leave my union-covered title or company?
            </h3>

            <ul class="list-disc space-y-2 pl-5">
              <li>
                Yes. If you become a WGAE member, you can maintain membership for up to 10 years by
                paying $40/quarter (which can be done online). You must opt in within 30 days of
                leaving the covered position, otherwise your membership will be deactivated. You can
                connect departing members with your WGAE staff rep.
              </li>
              <li>
                If you maintain your membership, you retain all WGAE-wide benefits (voting rights,
                event invitations, screeners, etc), but would not be covered by a union contract or
                have WGAE representation at a non-WGAE workplace.
              </li>
            </ul>
          </div>

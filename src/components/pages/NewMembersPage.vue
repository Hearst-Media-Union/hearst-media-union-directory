            Dues FAQ + Info
          </h2>

          <div class="space-y-2">
            <h3 class="text-base font-semibold text-(--color-brand-navy)">
              Who sets WGAE dues and what do they pay for?
            </h3>

            <ul class="list-disc space-y-2 pl-5">
              <li>
                WGAE dues are set by the
                <a
                  href="https://www.wgaeast.org/council/"
                  target="_blank"
                  rel="noreferrer"
                  class="guide-link"
                >
                  Council
                </a>
                (the governing body of the WGAE, made up of elected members).
              </li>
              <li>
                Dues enable the Union (which you are part of!) to organize unorganized workers and
                gain, build and sustain worker power in newsrooms, writers’ rooms, and online media
                spaces. Through organizing, organized workers have transformed the online media and
                print industry, where average wages were around $40k, to an average wage of $78k.
              </li>
            </ul>
          </div>

          <div class="space-y-2">
            <h3 class="text-base font-semibold text-(--color-brand-navy)">What are WGAE dues?</h3>

            <ul class="list-disc space-y-2 pl-5">
              <li>
                Dues are 1.5% of earnings + $40/quarter membership fee. The $40/quarter is split
                between paychecks (for most people, about $6/check).
              </li>
              <li>
                Upon leaving a union shop, individuals can decide to continue paying the $40/quarter
                fee to retain WGAE membership.
              </li>
              <li>Dues are deducted post tax, every paycheck</li>
            </ul>
          </div>

          <div class="space-y-2">
            <h3 class="text-base font-semibold text-(--color-brand-navy)">Why join the Union?</h3>

            <ul class="list-disc space-y-2 pl-5">
              <li>
                Unless you are in a “right to work” state, you must either join or pay an “agency”
                fee that is nearly equal to dues.
              </li>
              <li>
                WGAE membership means a strong union and strong shop floor democracy. The employer
                processes dues, so they know how many of us sign up.
              </li>
              <li>
                Paying your fair share enables the Union to fight for workplace democracy and power
                (see above). Plus, you will be able to vote on your next CBA, hold a leadership
                position in the Union, be nominated for the Writers’ Guild Awards, receive DVD
                screeners for “your consideration,” and attend the hundreds of WGAE screenings,
                events, and trainings for members.
              </li>
            </ul>
          </div>

          <div class="space-y-2">
            <h3 class="text-base font-semibold text-(--color-brand-navy)">
              What is the initiation fee?
            </h3>

            <ul class="list-disc space-y-2 pl-5">

          <h2 class="font-heading text-2xl font-semibold text-(--color-brand-navy)">
            What To Do if You Are Experiencing Workplace Harassment
          </h2>

          <p><em>Adopted from BetterBrave.org</em></p>

          <h3 class="text-base font-semibold text-(--color-brand-navy)">
            Make it clear that you want the harassment to stop.
          </h3>
          <p>
            If you experience something you believe to be harassment, you should immediately let
            that person know that their behavior made you uncomfortable.
          </p>

          <p><strong>Be firm, specific, Never apologize.</strong></p>
          <p>
            It might look something like this: "Hey, Josh, your remarks about my outfits on a daily
            basis make me uncomfortable. I'd prefer if you would stop making these comments." There
            are two benefits to calling out this behavior. One, it helps educate the person about
            what is inappropriate or uncomfortable behavior. Two, you've clearly communicated to the
            individual that you want their behavior to stop.
          </p>

          <p>
            If you don't feel comfortable directly confronting this individual or the harassment
            continues despite your efforts to tell the person to stop, be sure to follow the
            suggestions below.
          </p>

          <h3 class="text-base font-semibold text-(--color-brand-navy)">
            Reach out to your Union Rep!
          </h3>
          <p>
            In an ideal world, your Human Resources has processes in place to address your situation
            appropriately. Oftentimes, they do handle reports of harassment swiftly and
            appropriately.
          </p>
          <p>Unfortunately, there are instances where that’s simply not the case.</p>
          <p>
            <strong
              >If you are in the Hearst Media Union, you have the right to bring a Union Rep to
              meetings with HR to file a harassment complaint.</strong
            >
          </p>
          <p>
            Once you’ve contacted your union rep, they will help ensure that your documentation is
            robust and organized, and protect you against potential retaliation. Your union rep will
            work with you to ensure that your complaint sends a clear signal to your employer that
            you are informed of your rights and taking steps to protect yourself.
          </p>

          <h3 class="text-base font-semibold text-(--color-brand-navy)">
            Make sure to document everything!
          </h3>
          <p>
            Before contacting HR, be sure to document your interactions and contact your union rep.
            <strong
              >We want you to be as informed and protected as possible before you take the step of
              talking to HR.</strong
            >
          </p>
          <p>
            Write down your story and document everything—we mean everything! Keep a written record
            of all interactions you have with the harasser. This doesn’t have to be fancy—you can
            scribble it down on a piece of paper, but be sure to always write the date, and include
            as much detail as you can. What happened? Where did it happen? Who else heard or saw it
            happen? If you talked about the situation with anyone at work, write down those
            conversations with the same amount of detail, too.
          </p>
          <p>
            Save all evidence of harassment. Print emails. Take screenshots of text messages. Backup
            your phone. You should even save text messages or emails you sent to friends or family
            about what happened, and keep track of who you’ve discussed it with.
          </p>


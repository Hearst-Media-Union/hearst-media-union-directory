        <div class="guide-content">
          <h2 class="font-heading text-2xl font-semibold text-(--color-brand-navy)">
            Terms to Know
          </h2>

          <p>
            <strong>Arbitration:</strong> Third party dispute resolution, in the event that a
            contract grievance is not resolved the parties can seek binding arbitration to settle
            the matter
          </p>
          <p>
            <strong>Bargaining Unit:</strong> Group of people covered by one contract (literally, a
            unit of people who bargain together)
          </p>
          <p>
            <strong>Collective Bargaining Agreement (CBA):</strong> Official term for union
            contract. It’s what it sounds like - an agreement that gets bargained collectively. Most
            union contracts are three years. WGAE contracts in digital media currently include: GMG,
            VICE, ThinkProgress, Salon, HuffPost, The Intercept, and Thrillist.
          </p>
          <p>
            <strong>Council of the Writers Guild of America, East:</strong> The governing body of
            the Writers Guild of America, East composed of members elected by other members. Any
            member in good standing (which means paying dues) can vote for Council. Any member in
            good standing for at least one year can run for Council. Council members serve a
            two-year term. Elections are every August and conclude in September. For more
            information about Council:
            <a
              href="https://www.wgaeast.org/council-elections/"
              target="_blank"
              rel="noreferrer"
              class="guide-link"
              >https://www.wgaeast.org/council-elections/</a
            >
          </p>
          <p>
            <strong>Dues:</strong> All unions are funded by membership dues, which pay for things
            like negotiations, contract enforcement, and member events. WGAE dues are set by the
            elected WGAE Council. Dues for all WGAE members are 1.5% of earnings + $40/quarter.
            There is also a $500 initiation fee for new union members, which can be spread out in a
            payment plan.
          </p>
          <p><strong>Grievance:</strong> Process for filing and resolving CBA violations</p>
          <p>
            <strong>Minimum Basic Agreement:</strong> A collective bargaining agreement with the
            Alliance of Motion Picture and Television Producers (AMPTP), the entertainment
            industry’s collective bargaining representative. The contract covers screen,TV, and new

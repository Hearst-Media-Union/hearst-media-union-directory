            Know your rights
          </p>

          <h2 class="font-heading text-2xl font-semibold text-(--color-brand-navy)">
            Weingarten Rights
          </h2>

          <p>
            The US Supreme Court has ruled that the National Labor Relations Act gives workers the
            <strong>right to request union representation during investigatory interviews</strong>
            by supervisors, security personnel, and other managerial staff.
          </p>

          <p>An investigatory interview occurs if:</p>

          <ol class="list-decimal space-y-2 pl-5">
            <li>management questions you to obtain information;</li>
            <li>
              you have a reasonable apprehension that your answers could be used as the basis for
              discipline or other adverse action.
            </li>
          </ol>

          <p>
            These points apply to all conversations with your manager, including informal-seeming
            ones in Slack or other chat platforms. You must ask for union representation either at
            the beginning of or during the interview. Management does not have to remind you of this
            right. If your request is refused and management continues asking questions, you may
            refuse to answer. If this happens, your employer is guilty of an unfair labor practice
            and charges may be filed. If you are called to a meeting with management, you can read
            (or write) the following:
          </p>

          <blockquote class="border-l-4 border-(--color-brand-red) pl-4 font-semibold">
            “If my responses to your questions could lead to me being disciplined or terminated, or
            adversely affect my personal working conditions, I respectfully request that you summon
            my union representative. Until my representative arrives, I choose not to answer any
            questions.”
          </blockquote>
        </div>
      </section>

